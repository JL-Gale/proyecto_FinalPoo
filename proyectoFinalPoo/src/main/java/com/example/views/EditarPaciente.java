    private void buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonActionPerformed

        String nombre = nameTxt.getText();
        String apellido_p = apPTxt.getText();
        String apellido_m = apMTxt.getText();
        String domicilio = domTxt.getText();
        String telefono = phoneTxt.getText();

        //Vakidacion de datos
        if (nombre.isEmpty() || apellido_p.isEmpty() || apellido_m.isEmpty() || domicilio.isEmpty() || telefono.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Debe llenar todos los campos. \n", "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
            nameTxt.requestFocus();
            return;
        }

        com.example.models.Pacientes paciente = editPacientes;
        paciente.setNombre(nombre);
        paciente.setApellido_p(apellido_p);
        paciente.setApellido_m(apellido_m);
        paciente.setDomicilio(domicilio);
        paciente.setTelefono(telefono);
              
        try {
            DAOPacientes dao = new DAOPacientesImpl();
            dao.modificar(paciente);
            
            javax.swing.JOptionPane.showMessageDialog(this, "Paciente editado exitosamente.\n", "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_buttonActionPerformed

